
function soma(x, y){
    return x+y;
}
//chamando a função
console.log(soma(3, 4));

const multiplicacao = function(x,y) {
    return x * y;
}
//chamando a função pela variável
console.log(multiplicacao(5,5));


//com arrow function =>

const dividir = (x, y) => {
    return x / y;
}
console.log(dividir(25,5));